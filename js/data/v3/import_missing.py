        fn = f'hsk3_lvl{js_lv}.js'
        # Read existing to find last closing bracket
        with open(fn, encoding='utf-8') as f:
            content = f.read()
        # Find insertion point: before the last ];
        insert_pos = content.rfind('];')
        if insert_pos == -1:
            print(f'ERROR: could not find ]; in {fn}')
            continue
        new_entries = []
        for h, p, vi, en in items:
            # Escape single quotes
            h2  = h.replace("'", "\\'")
            p2  = p.replace("'", "\\'")
            vi2 = vi.replace("'", "\\'") if vi else en[:80].replace("'","\\'")
