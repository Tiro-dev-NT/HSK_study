# ── Load master CSV ──────────────────────────────────────────────────────────
master = {}  # simplified -> {level, pinyin}
with open('hsk30_master.csv', encoding='utf-8') as f:
    for row in csv.DictReader(f):
        simp = row['Simplified'].strip()
        lv   = row['Level'].strip()
        pin  = row['Pinyin'].strip()
        for word in simp.split('|'):
            word = word.strip()
            if word:
                master[word] = {'level': lv, 'pinyin': pin}

# ── Load English meanings from TSV files ─────────────────────────────────────
eng_dict = {}  # simplified -> english
for fn in ['hsk30_l4_en.tsv','hsk30_l5_en.tsv','hsk30_l6_en.tsv','hsk30_l79_en.tsv']:
    with open(fn, encoding='utf-8') as f:
        for line in f:
            parts = line.strip().split('\t')
            if len(parts) >= 4:
                simp = parts[1].strip()
                eng  = parts[3].strip()[:120]  # cap length
                eng_dict[simp] = eng

